MODULE UTIL_TPHY_MOD

USE YOMPHY, ONLY : TPHY

INTERFACE ACDC_SAVE
MODULE PROCEDURE ACDC_SAVE_TPHY
END INTERFACE

INTERFACE ACDC_LOAD
MODULE PROCEDURE ACDC_LOAD_TPHY
END INTERFACE







CONTAINS

SUBROUTINE ACDC_SAVE_TPHY (SELF, KLUN)

IMPLICIT NONE
TYPE (TPHY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%NPHYREP
WRITE (KLUN) SELF%LGLT
WRITE (KLUN) SELF%LNEWD
END SUBROUTINE

SUBROUTINE ACDC_LOAD_TPHY (SELF, KLUN)

IMPLICIT NONE
TYPE (TPHY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

READ (KLUN) SELF%NPHYREP
READ (KLUN) SELF%LGLT
READ (KLUN) SELF%LNEWD
END SUBROUTINE













END MODULE
